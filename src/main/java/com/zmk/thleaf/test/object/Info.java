package com.zmk.thleaf.test.object;

public class Info {
    String key;
    String value;
    public  Info(String key, String vl) {
    	this.key = key;
    	this.value = vl;
    }
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
    
}
